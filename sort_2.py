# 파이썬 sort 함수를 사용한 정렬 예제 
#-------------------------------------------------------

test_list = [5, 8, 10, -1, -10, 100, 50, 90, 45, 64, 7,
             9, -20, -10, -1, 0, 255, 500, -300, -256]

# 원래 자료 출력 
print(test_list)

print("\nSorting...\n")

# 정렬 함수 호출 
test_list.sort()

# 정렬된 자료 출력 
print(test_list)

