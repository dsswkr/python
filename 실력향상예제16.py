# 실력 향상 예제 16

n1 = 0
while n1 < 1:
  n1 = int(input("0보다 큰 정수를 입력하세요: "))
  
n2 = 0
while n2 < 1:
  n2 = int(input("0보다 큰 정수를 입력하세요: "))
  
print("%i + %i = %i" %(n1, n2, n1+n2))
print("%i - %i = %i" %(n1, n2, n1-n2))
print("%i * %i = %i" %(n1, n2, n1*n2))
print("%i / %i = %i" %(n1, n2, n1/n2))      
print("결과 합 = %i" %(n1+n2+ n1-n2+ n1*n2+ n1/n2))  # n1 + n1 + n1*n2 + n1/n2

  
