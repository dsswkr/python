# 실력 향상 예제 78

def __gcd(p, q):
  return p if q == 0 else __gcd(q, int(p%q))

# 유클리드 호제법으로 최대공약수를 구하는 함수
def gcd(a, b):
  # a가 b보다 작지 않게 조정 
  return __gcd(a, b) if a > b else __gcd(b, a)  

#---------------------------------------------------
# 메인 처리
#---------------------------------------------------

a = 76
b = 288;  

# 76, 288 --> 4 
# 72, 246 --> 6
  
print("%i와 %i의 최대공약수 = %i" %(a, b, gcd(a, b)))


