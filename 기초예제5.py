# 기초 예제 5
import math

num = 9.5

# 결과를 정수형으로 출력 
print("9.5의 올림 값 = %d" %(math.ceil(num)))
print("9.5의 내림 값 = %d" %(math.floor(num)))
print("9.5의 반올림 값 = %d" %(round(num)))

