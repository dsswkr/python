# 실력 향상 예제 23-1

num = 12586

# 10진수를 16진수로 출력  
print("10진수 %i ==> 16진수 %s" %(num, hex(num)))
#결과: 10진수 12586 ==> 16진수 0x312a

# 10진수를 8진수로 출력  
print("10진수 %i ==> 8진수 %s" %(num, oct(num)))
#결과: 10진수 12586 ==> 8진수 0o30452

# 10진수를 2진수로 출력   
print("10진수 %i ==> 2진수 %s" %(num, bin(num)))
#결과: 10진수 12586 ==> 2진수 0b11000100101010

# 116진수를 10진수로  출력 
print("%s ==> 10진수 %d" %('F', int('F', 16)))
#결과: F ==> 10진수 15 
